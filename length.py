## LENGTH CONVERSIONS
def mm_to_in(mm):
    """
    Convert milimeters to inches
    """
    return mm / 25.4


def cm_to_in(cm):
    """
    Convert centimeters to inches
    """
    return cm / 2.54


def m_to_f(meters):
    """
    Convert meters to feet
    """
    return meters / 0.3048


def in_to_mm(inches):
    """
    Convert inches to milimeters
    """
    return inches * 25.4


def in_to_cm(inches):
    """
    Convert inches to centimeters
    """
    return inches * 2.54


def f_to_m(feet):
    """
    Convert feet to meters
    """
    return feet * 0.3048
